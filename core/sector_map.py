def get_sector(ticker: str) -> str:
    """티커 → 섹터. 미등록 시 '기타' 반환."""
    return SECTOR_MAP.get(ticker, "기타")


def has_sector_overlap(new_ticker: str, existing_tickers: list[str]) -> tuple[bool, str]:
    """
    new_ticker 가 existing_tickers 중 하나와 같은 섹터인지 확인.
    반환: (중복 여부, 충돌 종목명 또는 빈문자)
    """
    new_sector = get_sector(new_ticker)
    if new_sector == "기타":
        return False, ""   # 미분류는 차단 안 함 (false positive 방지)
    for t in existing_tickers:
        if get_sector(t) == new_sector:
            return True, t
    return False, ""
