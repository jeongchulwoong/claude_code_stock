ALL: dict[str, str] = {**DOMESTIC, **FOREIGN}

# reverse lookup: ticker → name
TICKER_TO_NAME: dict[str, str] = {v: k for k, v in ALL.items()}


def get_ticker(name: str) -> str | None:
    return ALL.get(name)


def get_name(ticker: str) -> str:
    return TICKER_TO_NAME.get(ticker, ticker)


def resolve(name_or_ticker: str) -> tuple[str, str]:
    if name_or_ticker in ALL:
        return ALL[name_or_ticker], name_or_ticker
    if name_or_ticker in TICKER_TO_NAME:
        return name_or_ticker, TICKER_TO_NAME[name_or_ticker]
    return name_or_ticker, name_or_ticker


def is_domestic(ticker: str) -> bool:
    return ticker.endswith(".KS") or ticker.endswith(".KQ")


# ── 카테고리 빌더 (소스 코멘트 헤더 기반) ───────────────
def _build_categories() -> dict[str, list[str]]:
    """파일의 '# ── 헤더 ──' 코멘트 사이 종목들을 묶어 카테고리 dict 반환."""
    import re, pathlib
    src = pathlib.Path(__file__).read_text(encoding="utf-8")
    cats: dict[str, list[str]] = {}
    current = None
    header_re = re.compile(r'^\s*#\s*──+\s*(.+?)\s*──+')
    name_re   = re.compile(r'^\s*"([^"]+)"\s*:\s*"[^"]+"')
    in_dict   = False
    for line in src.splitlines():
        if line.startswith("DOMESTIC") or line.startswith("FOREIGN"):
            in_dict = True
            continue
        if not in_dict:
            continue
        if line.startswith("}") and not line.startswith("    }"):
            in_dict = False; current = None; continue
        m = header_re.match(line)
        if m:
            h = m.group(1).strip()
            # skip top-level "국내/해외" wrapper headers
            if h in ("국내 (KOSPI / KOSDAQ)", "해외"):
                current = None
            else:
                current = h
                cats.setdefault(current, [])
            continue
        if current:
            mn = name_re.match(line)
            if mn:
                cats[current].append(mn.group(1))
    # 빈 카테고리 제거
    return {k: v for k, v in cats.items() if v}


CATEGORIES: dict[str, list[str]] = _build_categories()
